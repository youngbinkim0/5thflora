// Initialize gallery when DOM is ready
document.addEventListener('DOMContentLoaded', () => {
    // Check if gallery has data
    if (galleryData.length > 0) {
        // Initialize gallery
        const gallery = new Gallery('galleryGrid', galleryData);
    } else {
        // Show placeholder message
        const galleryGrid = document.getElementById('galleryGrid');
        const placeholder = document.createElement('div');
        placeholder.style.cssText = 'grid-column: 1/-1; text-align: center; padding: 4rem 2rem; color: var(--color-soft-gray);';
        placeholder.innerHTML = `
            <p style="font-family: var(--font-primary); font-size: 1.5rem; margin-bottom: 1rem;">Gallery Coming Soon</p>
            <p>Add your curated photos to the <code>galleryData</code> array in <code>js/main.js</code></p>
        `;
        galleryGrid.appendChild(placeholder);
    }

    // Smooth scroll for navigation links
    document.querySelectorAll('.nav a').forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const targetId = link.getAttribute('href');
            scrollToSection(targetId);
        });
    });

    // Add scroll animations for sections
    setupScrollAnimations();
});

// Scroll animations - exclude gallery section
function setupScrollAnimations() {
    const sections = document.querySelectorAll('section:not(.gallery-section)');

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, { threshold: 0.1 });

    sections.forEach(section => {
        section.style.opacity = '0';
        section.style.transform = 'translateY(20px)';
        section.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
        observer.observe(section);
    });
}
